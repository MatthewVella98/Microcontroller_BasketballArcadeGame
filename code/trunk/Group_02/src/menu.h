/*!
 * \file      menu.h
 * \brief     Contains the methods that handle the main menu
 */
 
 #include "lcd.h"

/*! This enum is used for switching menu states. */
typedef enum{
	up = 1,
	down = 0
} Direction;

/*! \brief Changes the menu choice depending on the direction specified.
 *
 *  \param d   Direction to change menu.
 */
void change_state(Direction d);

/*! \brief Sets the state to 0 (default state).
 *
 */
void init_menu();

/*! \brief Accepts the current state as the menu choice and enters that menu choice.
 *
 */
void enter_state();